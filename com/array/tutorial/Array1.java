package com.array.tutorial;

public class Array1 {

	public static void main(String[] args) {
		int[] age = new int[10]; // creating arry of length 10
		System.out.println(age[0]); // array starting index is 0 
        System.out.println(age[1]);
        System.out.println(age[2]);
        System.out.println(age[3]);
        System.out.println(age[4]);
        System.out.println(age[5]);
        System.out.println(age[6]);
        System.out.println(age[7]);
        System.out.println(age[8]);
        System.out.println(age[9]); // array ending index is 9 because n-1 here n means array length  
        System.out.println(age[10]);
	}

}
