package total;

public class datat {

	public static void main(String[] args) {
		  byte n = -128;// size 1 byte range -128 to 127
		  short j = 4100;// size 2 bytes
		  int m = 41;// 4 bytes
		  long l = 1234586;// 8 bytes
		  float f = 4.1f;//4 bytes
		  double a = 41.596d;// 8 bytes
		  String g = "goldee";
		  char h = 's';// 2 bytes 
		  boolean t = true;// 1 bit
		  System.out.println("byte: "+n);
		  System.out.println("short: "+j);
		  System.out.println("int: "+m);
		  System.out.println("long:"+l);
		  System.out.println("float: "+f);
		  System.out.println("double: "+a);
		  System.out.println("string:"+ g);
		  System.out.println("char: "+h);
		  System.out.println("boolean:"+ t);


	}

}
